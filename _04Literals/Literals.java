package _04Literals;

public class Literals {
    public static void main(String[] args) {
        int age = 23;
        byte age1 = 34;
        long age2 = 78999099034309L;
        short age3 = 89;
        char ch = 'K';
        float num = 89.43f;
        double d1 = 89.34d;
        boolean a = true;
        String str = "kamal";
        System.out.println(a);
        System.out.println(num);
        System.out.println(ch);
        System.out.println(age1);
        System.out.println(str);
    }
}

