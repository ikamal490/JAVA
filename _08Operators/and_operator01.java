/*
    AND(&&) operator
    Agar ek bhi condition false hoti hai toh false return karega
 */
public class and_operator01 {
    public static void main(String[]args){
        System.out.println(34>=23 && 45>23);  // It give true
        System.out.println(45<=23 && 67>23); // It give False

    }
}
