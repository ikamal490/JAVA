package _08Operators;
/*
    Or (||) operator
    Agar ek bhi condition true hoti hai toh true return karega
 */
public class or_operator {
    public static void main(String[] args) {
        System.out.println(34 >= 23 || 45 > 23);  // It give true
        System.out.println(45 <= 23 || 67 > 23); // It give true
    }
}
