package _14_string_methods;

public class charAt {
    public static void main(String[] args) {
        String name = "SHEFALI chopra ji ";
        System.out.println(name.charAt(0)); // It starts with the index value
        System.out.println(name.charAt(1));
        System.out.println(name.charAt(3));
        System.out.println(name.charAt(13));
    }
}
