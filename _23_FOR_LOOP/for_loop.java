package _23_FOR_LOOP;

public class for_loop {
    public static void main(String[]args){
        for (int i = 1;  i<=10; i++){
            System.out.println(i);
        }

        System.out.println();
        // Decrement in the Loop :

        for(int i = 10; i>0; i--){
            System.out.println(i);
        }


    }
}
